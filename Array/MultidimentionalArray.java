package Array;

public class MultidimentionalArray {
    public static void main(String[] args) {
        int[][] mul1 = {{1,2},{2,3}};

        for(int i =0 ; i< mul1.length ; i++){
            for(int j = 0;j<mul1[i].length ; j++){

                System.out.println(mul1[i][j]);
            }
            

        }

    }
}
